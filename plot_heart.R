f1<-function(x)
{
y<-sqrt(1-(abs(x)-1)^2)
return(y)
}
################################################################################################################################################
f2<-function(x)
{
y<-acos(1-abs(x))-pi
return(y)
}
################################################################################################################################################
curve(expr=f1, from=-2, to=2, ylim=c(-3.3,1.5), col="red", lwd=5)
curve(expr=f2, from=-2, to=2, add=T,  col="red", lwd=5)
